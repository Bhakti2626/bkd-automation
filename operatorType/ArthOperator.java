package operatorType;

public class ArthOperator {
	
	public static void main(String args[]) {
		
		int a = 20;
		int b = 7;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		System.out.println(50/15);
		System.out.println(50%15);
		
	}

}
