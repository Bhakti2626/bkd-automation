package firstDatatype;

public class DatatypeExample {
	
	public static void main(String args[]) {
		
		boolean a; //variable declaration statrt with small letter
		a= true;  // variable assignment 
		System.out.println(a);
	
		boolean a1 = false; //variable declaration with assignment
		
		System.out.println(a);
		System.out.println(a1);
		
		// byte data type
		
		byte b1 = 50;
		System.out.println(b1);
		
		byte b2 = 127;
		byte b3 = 100;
		System.out.println(b2 + b3);
		
		// short datatype
		short s1 = 31500;
		System.out.println(s1);
		
		// int datatype
		
		int i = 223890;
		System.out.println(i);
		
		int i1 = -245679;
		System.out.println(i1);
		
		//
		
		char ch = 's';
		
		String sh = "Shiv";
		
	}
	


}
